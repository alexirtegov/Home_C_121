/*
E19-Цифры по возрастанию

Вывести в порядке следования цифры, входящие в десятичную запись натурального числа N.

Input format
Одно натуральное число N

Output format
Цифры через пробел


Input
54412
Output
5 4 4 1 2
 */


#include <stdio.h>

int main(void) {
    long long n; // Используем long long на случай очень большого числа
    int digits[20]; // Массив для хранения цифр (в числе long long максимум 19 цифр)
    int count = 0;  // Счетчик количества цифр

    // Считываем число
    if (scanf("%lld", &n) != 1) {
        return 1;
    }

    // Обработка натуральных чисел (N > 0)
    // Извлекаем цифры с конца
    while (n > 0) {
        digits[count] = n % 10; // Берем последнюю цифру
        n = n / 10;             // Отбрасываем последнюю цифру от исходного числа
        count++;                // Увеличиваем счетчик цифр
    }

    // Теперь в массиве digits лежат цифры числа, но в обратном порядке.
    // Чтобы вывести их как в оригинале, проходимся по массиву с конца до начала (от count-1 до 0).
    for (int i = count - 1; i >= 0; i--) {
        printf("%d ", digits[i]);
    }

    return 0;
}
