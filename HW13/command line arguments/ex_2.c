/*
Этот код делает то же самое, что и ваш первый пример (читает параметры из командной строки), 
но использует профессиональный и более удобный инструмент — встроенную функцию getopt. 
Вместо того чтобы вручную перебирать массив через for и искать минусы, getopt делает всю грязную работу за вас.

Давайте ответим на ваши вопросы по порядку, а затем разберем примеры.
Нужны ли здесь знаки минус (-) как флаги?
Да, обязательно. Функция getopt специально создана (она так запрограммирована внутри операционной системы) искать только те аргументы, 
которые начинаются с дефиса (-). Все, что без дефиса, она будет воспринимать как просто текст или значения флагов.

Почему внутри скобок написано "ab:C::dh" и может ли запись быть другой?
Да, эта строка может быть абсолютно любой, вы придумываете ее сами! Это строка конфигурации (правил). С помощью нее вы объясняете функции getopt, какие именно буквы-флаги может понимать ваша программа. 
Каждый символ в этой строке имеет свой смысл:

Просто буква (например, a, d, h) означает обычный флаг-включатель. Если в строке есть эти буквы, значит программа поймет команды -a, -d и -h.

Буква с одним двоеточием (например, b:) означает, что это флаг, требующий обязательного значения после себя. 
То есть программа будет ждать что-то вроде -b 100 или -b text.

Буква с двумя двоеточиями (например, C::) означает, что значение после флага необязательно. 
Можно написать просто -C, а можно передать значение. Важный нюанс: для флагов с двумя двоеточиями значение пишется слитно, 
без пробела, например -C50.

Что означает загадочная строка while ((rez = getopt(...)) != -1)?
Эта конструкция делает три действия одновременно:
Вызывает функцию getopt, которая берет следующий аргумент и проверяет его по нашим правилам "ab:C::dh".
Результат этой проверки (найденную букву) она сохраняет в переменную rez.
Затем проверяется условие != -1. Когда getopt прочитает все аргументы до самого конца и больше параметров не останется, 
она вернет число -1. Это сигнал для цикла while, что пора останавливаться.

Откуда берется переменная optarg?
В строках с -b и -C вы можете заметить слово optarg (например: printf("... = %s", optarg)). Вы не объявляли эту переменную. 
Она автоматически предоставляется библиотекой <unistd.h>. 
Как только getopt находит флаг со значением (например, -b 42), она заботливо кладет текст "42" в переменную optarg, 
чтобы вы могли его вывести или использовать.
Если пользователь введет неизвестный флаг (которого нет в правилах, например -x), 
умная функция getopt вернет знак вопроса ?, и программа перейдет в блок ошибке case '?'.
 */

#include <stdio.h>      // Для функций ввода/вывода (printf)
#include <unistd.h>     // Для getopt() (обработка аргументов)

int main(int argc, char *argv[])
{
    int rez = 0;  // Переменная для хранения текущей опции
    
    // Цикл обработки аргументов. "ab:C::dh" - строка опций:
    // a, d, h - флаги без параметров
    // b: - обязательный параметр
    // C:: - необязательный параметр
    while ((rez = getopt(argc, argv, "ab:C::dh")) != -1)
    {
        switch (rez)
        {
            case 'a': 
                printf("found argument \"a\".\n"); 
                break;
                
            case 'b': 
                printf("found argument \"b = %s\".\n", optarg); //например для -b 99 Функция getopt увидит -b положит его в optarg и перейдет в case 'b'. Выведется "found argument b = 99".
                break;
                
            case 'C': 
                printf("found argument \"C = %s\".\n", optarg); 
                break;
                
            case 'd': 
                printf("found argument \"d\"\n"); 
                break;
                
            case 'h':  // Новая опция для справки
                printf("Usage: %s [OPTIONS]\n", argv[0]);
                printf("Options:\n");
                printf("  -a\t\tEnable feature A\n");
                printf("  -b VALUE\tSpecify value for B (required)\n");
                printf("  -C [VAL]\tOptional value for C\n");
                printf("  -d\t\tEnable debug mode\n");
                printf("  -h\t\tShow this help message\n");
                return 0;  // Завершаем программу после вывода справки
                
            case '?': 
                printf("Error: Unknown option or missing argument!\n");
                printf("Try '%s -h' for help.\n", argv[0]);
                return 1;  // Завершаем с ошибкой
        }
    }
    return 0;  // Успешное завершение
}
