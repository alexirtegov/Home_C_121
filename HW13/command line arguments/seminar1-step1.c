/*
Этап 1: Базовая структура и ввод пути
Объяснение:
Этот фрагмент программы преобразует пользовательский ввод пути в абсолютный путь и гарантирует, 
что путь заканчивается символом /.

Цель: Преобразование введённого пути в абсолютный формат.
Функция convert_path_to_full:
Если путь начинается с / (абсолютный) — копируем как есть.
Если начинается с . — используем текущую директорию (getcwd).
Иначе — комбинируем текущую директорию с введённым путём.
Всегда добавляем / в конец для корректной конкатенации имён файлов.

Для чего это нужно?
Этот код подготавливает пути для последующей обработки в файловых операциях, где требуется:

Единый формат путей
Гарантированное наличие / в конце для конкатенации с именами файлов
Преобразование пользовательского ввода в абсолютные пути

Ввод пользователя	Текущая директория	Результат (вывод)
	/usr				Любая		    	/usr/
	
	  .				/home/user			  /home/user/
	  
 Documents			  /home			    /home/Documents/
 
   ./src	          /projects			/projects/src/
  
  ../parent			  /home/user		/home/user/../parent/
*/

#include <stdio.h>   // Нужна для printf (вывод на экран) и scanf (чтение ввода)
#include <string.h>  // Инструменты для работы со строками (strlen, strcpy, strcat)
#include <unistd.h>  // Инструменты операционной системы (например, getcwd - где я нахожусь?)

#include <unistd.h> 
//для getcwd:
//Мы объявляем буфер `full_path` размером `PATH_LENGTH` (256 байт). 
//Функция `getcwd` (get current working directory) записывает в этот буфер текущий рабочий каталог.

//Параметры функции `getcwd`:
//- Первый параметр: указатель на буфер, куда будет записан путь.
//- Второй параметр: размер этого буфера.


#define PATH_LENGTH 256
#define STR255 "%255s"

void convert_path_to_full(char *full_path, const char *dir) {
	//В этой функции программа пытается угадать, что именно имел в виду пользователь, 
	//анализируя первый символ (dir[0]) того, что он ввел. Возможны три варианта.
	
    if (dir[0] == '/') {	//Вариант А: Идеальный пользователь (Ввел абсолютный путь)
        strcpy(full_path, dir);         //Пример: Пользователь ввел /user/docs.

		//Работа условия: dir[0] = '/' → условие истинно ✅ strcpy(full_path, "/user/docs")
		//Функция strcpy (string copy) берет всё, что лежит в dir ("/user/docs"), и перекладывает в full_path.


    } else if (dir[0] == '.') { //Вариант Б: Ленивый пользователь (Поставил точку)
        getcwd(full_path, PATH_LENGTH);  // Пример: Пользователь ввел . (обозначение текущей папки в Linux/Windows).
        //Логика: Программа понимает, что пользователь хочет путь к папке, в которой он прямо сейчас находится. 
        //Она вызывает getcwd ("get current working directory" — "дай ту папку, где я сейчас работаю"). 
        //Эта системная функция сама узнает полный путь (например, /home/user/projects) и кладет его в full_path.
        
    } else { //Вариант В: Обычный пользователь (Ввел имя папки)
		//Пример: Пользователь находится в /home/user и ввел просто Documents.
        getcwd(full_path, PATH_LENGTH);  // программа узнает, где мы: full_path становится равен "/home/user".
        strcat(full_path, "/");         //функция strcat (string concatenate, "склеить строки") приклеивает слэш в конец. full_path становится "/home/user/".
        strcat(full_path, dir);	//приклеиваем то, что ввел пользователь. full_path становится "/home/user/Documents".

    }
    // Добавляем / в конец если отсутствует
    if (full_path[strlen(full_path) - 1] != '/')
    //мы читаем full_path и считаем длину массива, сравниваем последний с '/' и если '/' нет, добавлем
        strcat(full_path, "/"); //добавляем '/' если его нет
}

int main() 
 {
	char dir[PATH_LENGTH];       // Создаем "коробку" для ввода пользователя (массив на 256 символов)
    char full_path[PATH_LENGTH]; // Создаем "коробку" для итогового результата (тоже на 256)
    
	printf("Input dir: ");       // Печатаем приглашение
    scanf(STR255, &dir[0]);      // Читаем ввод
    
    convert_path_to_full(full_path, dir); //передаем два массива один пустой второй содержит строку
    
    printf("Full path: %s\n", full_path);
    
    return 0;
}
