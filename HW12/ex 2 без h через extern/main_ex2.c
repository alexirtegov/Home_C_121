/*
Отличный вопрос! Вы затронули одну из самых важных тем в языке C — разделение этапов компиляции и линковки (компоновки). 
Секрет кроется вот в этой строчке в вашем файле main_ex2.c:
C

Копировать код

extern int max(int a, int b);

Отвечая кратко: заголовочные файлы (.h) в языке C вообще не обязательны. Они существуют исключительно для удобства. 
Давайте разберем, как это работает «под капотом».
Как работает компилятор (почему это работает сейчас)
Компилятор обрабатывает .c файлы строго по одному. Когда он собирает main_ex2.c 
в объектный файл main_ex2.o (по правилам вашего Makefile), он ничего не знает о существовании файла max_ex2.c. 
Когда компилятор доходит до строки int m = max(a, b);, он должен запаниковать и выдать ошибку: 
"Что такое max? Какие аргументы эта функция принимает? Что возвращает?".
Но вы спасаете ситуацию строчкой extern int max(int a, int b);. 
Это называется объявлением (прототипом) функции. Слово extern говорит компилятору:
> "Поверь мне на слово, где-то в другом месте программы есть функция max, она принимает два int и возвращает int. 
Оставь тут пустое место, позже разберемся".
Компилятор верит вам, делает файл main_ex2.o с "пустым местом" вместо функции max и идет дальше компилировать max_ex2.c в max_ex2.o.

В дело вступает Линкер (Компоновщик)
Посмотрите на эту строку в вашем Makefile:
gcc -o main_ex2.exe main_ex2.o max_ex2.o
Здесь работает компоновщик. Он берет оба откомпилированных кусочка (.o файлы) и сшивает их вместе в один .exe. Он видит: 
В main_ex2.o кто-то пытался вызвать max и оставил пустое место.
В max_ex2.o лежит реальный исполняемый код функции max.
Компоновщик соединяет этот вызов с реальным кодом. Все счастливы, программа работает.
Зачем же тогда нужны .h файлы?
Если можно просто написать extern int ... в самом .c файле, зачем плодить .h файлы?

Представьте, что вы написали математическую библиотеку, в которой 50 функций. 
И у вас есть 10 разных файлов main1.c, engine.c, physics.c и т.д., и во всех них нужно использовать эти функции.
Если не делать .h файлов, вам придется в каждый из десяти .c файлов вручную скопировать 50 строк с extern int...! 
А если вы решите изменить аргументы одной функции, вам придется искать и исправлять это в 10 разных файлах. Это ужасно неудобно.
Файл .h решает эту проблему:
Вы пишете все 50 строк объявлений один раз в файл math_lib.h.
В остальных местах вы просто пишете #include "math_lib.h".
Директива #include делает очень простую вещь: она перед компиляцией тупо копирует весь текст из файла .h и вставляет его на свое место. 
То есть, когда вы используете #include, для компилятора результат выглядит абсолютно так же, как ваш текущий код с ручным объявлением extern.

Итог:
Первый случай (с .h файлом): Вы перепоручили работу по копированию объявлений функций директиве #include. Это профессиональный подход для больших программ.
Второй случай (ваш текущий код): Вы написали объявление руками (extern int max...). Это работает абсолютно так же, но подходит только для крошечных программ, где всего 1-2 функции.
 */


#include <stdio.h>

//ручное объявление функции из другого модуля
extern int max(int a, int b);

int main(int argc, char **argv)
{
	int a, b;
	printf("please, input two meaning\n");
	scanf("%d %d", &a, &b);
	int m = max(a, b);
	
	printf("max = %d", m);
	
	return 0;
}
