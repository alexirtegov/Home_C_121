

/*
Вот пошаговое объяснение каждой строки Makefile:

all: prog
*Определяет цель по умолчанию all, которая зависит от prog
*При запуске make без аргументов будет выполнена эта цель


prog: main.o func.o
*Цель prog (исполняемый файл) зависит от двух объектных файлов
*Если main.o или func.o изменились, prog будет пересобран

gcc -o prog main.o func.o
*Команда сборки: линкует объектные файлы в исполняемый prog
*-o prog явно задаёт имя выходного файла


main.o: main.c func.h
*Объектный файл main.o зависит от исходника main.c и заголовочного файла func.h
*При изменении любого из этих файлов main.o будет перекомпилирован

gcc -c -o main.o main.c
Команда компиляции:
*-c - компиляция без линковки
*-o main.o - задаёт имя выходного объектного файла
*main.c - исходный код


func.o: func.c
*Объектный файл func.o зависит только от func.c
(Предполагается, что func.c не зависит от других заголовков)

gcc -c -o func.o func.c
*Аналогично пункту 5: компиляция func.c в объектный файл


clean:
Вспомогательная цель для очистки проекта
Не имеет зависимостей

del *.o
Команда удаления всех объектных файлов (*.o)
Windows-версия команды (в Linux/macOS используйте rm -f *.o)

del main.exe
Удаление исполняемого файла
Важно: должно соответствовать имени в цели prog (лучше использовать del prog или del prog.exe)


Способ 1: Сборка и запуск через терминал (Рекомендуемый для понимания процесса)
Это классический способ работы с Makefile. Ваш скриншот с меню правой кнопки мыши идеально подходит.
В папке ex1 нажмите правую кнопку мыши по пустому месту и выберите «Открыть в Терминале».
В появившемся черном окне введите команду:
make
(и нажмите Enter)
Вы увидите, как в консоли пробегут команды gcc. В вашей папке появятся файлы main.o, func.o и готовая программа main.exe.
Теперь, чтобы запустить вашу программу, введите в терминале:
main.exe
(и нажмите Enter)
Программа будет ждать ввода. Введите два числа (например, 5 10) и нажмите Enter, чтобы проверить, как работает ваша функция max.
Примечание: Если на шаге 2 терминал напишет, что команда make не найдена, попробуйте ввести mingw32-make. 
В Windows инструменты программирования на С часто используют именно это имя.

Способ 2: Настройка и сборка прямо внутри Geany
Чтобы вам не приходилось каждый раз открывать терминал, мы можем настроить Geany на работу с вашим Makefile, используя скриншот настроек, который вы прислали.
Шаг 1: Правильно собираем программу
Вместо кнопок "Скомпилировать" или "Сборка" (которые работают для одного файла), вам нужно использовать команду Make.
В верхнем меню Geany выберите: Сборка -> Собрать (или нажмите Shift+F9).
Эта команда заставит Geany запустить ваш Makefile. В нижней панели сообщений должно быть написано, что сборка прошла успешно.
Шаг 2: Исправляем команду запуска в настройках
На вашем последнем скриншоте (настройки команд):
Посмотрите в самый низ на блок «Выполнить команды».
Там в строке Execute сейчас написано "./%e". Это формат для Linux.
Измените это значение на: "%e.exe" (кавычки нужны).
Обозначение %e — это имя вашего открытого файла без расширения (то есть main), соответственно %e.exe превратится в main.exe, и Windows поймет эту команду.
Нажмите OK.
Теперь вы можете открывать файл main.c, нажимать Shift+F9 для сборки, а затем привычную вам кнопку "Запустить" (или F5) для выполнения программы.
*/

